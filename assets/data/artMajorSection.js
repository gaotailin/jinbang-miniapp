function rankOf(seg, score) {
  const p = seg.points
  if (score >= p[0][0]) return p[0][1]
  if (score <= p[p.length - 1][0]) return seg.total
  for (let i = 0; i < p.length - 1; i++) {
    if (score <= p[i][0] && score > p[i + 1][0]) {
      const frac = (p[i][0] - score) / (p[i][0] - p[i + 1][0])
      return Math.round(p[i][1] + frac * (p[i + 1][1] - p[i][1]))
    }
  }
  return seg.total
}
module.exports.rankOf = rankOf
