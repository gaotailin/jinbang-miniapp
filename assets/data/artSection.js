// 综合分 → 全省位次（分段点线性插值；points 降序）
function rankOf(seg, comp) {
  const p = seg.points
  if (comp >= p[0][0]) return p[0][1]
  if (comp <= p[p.length - 1][0]) return seg.total
  for (let i = 0; i < p.length - 1; i++) {
    if (comp <= p[i][0] && comp > p[i + 1][0]) {
      const frac = (p[i][0] - comp) / (p[i][0] - p[i + 1][0])
      return Math.round(p[i][1] + frac * (p[i + 1][1] - p[i][1]))
    }
  }
  return seg.total
}

module.exports.rankOf = rankOf
